#!/usr/bin/env node
/**
 * Stop hook: after every task, scan the source tree for configuration values and
 * keep .env.example in sync.
 *
 *  1. Every `process.env.KEY` reference becomes a key in .env.example.
 *  2. Hardcoded-looking config (external URLs, API keys, tokens, secrets) is
 *     reported and listed as a TODO comment so it can be moved into env.
 *
 * Existing placeholder values in .env.example are preserved. The file is only
 * rewritten when the content actually changes.
 */

const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const SCAN_DIRS = ['app', 'lib', 'components', 'hooks', 'src', 'utils'];
const SCAN_EXT = new Set(['.ts', '.tsx', '.js', '.jsx', '.mts', '.mjs', '.cjs']);
const SKIP_DIRS = new Set(['node_modules', '.next', '.git', 'dist', 'build', 'coverage', '.specify']);
const OUT = path.join(ROOT, '.env.example');

/** URLs that are never configuration. */
const URL_ALLOWLIST =
  /localhost|127\.0\.0\.1|0\.0\.0\.0|(^|\/\/)(www\.)?(w3\.org|example\.(com|org)|schema\.org|json-schema\.org|github\.com|nextjs\.org|npmjs\.com|developer\.mozilla\.org)/i;

const SECRETISH_KEY = /(api[_-]?key|secret|token|password|passwd|credential|private[_-]?key|access[_-]?key|auth)/i;

function walk(dir, out = []) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return out;
  }
  for (const e of entries) {
    if (e.name.startsWith('.') || SKIP_DIRS.has(e.name)) continue;
    const full = path.join(dir, e.name);
    if (e.isDirectory()) walk(full, out);
    else if (SCAN_EXT.has(path.extname(e.name))) out.push(full);
  }
  return out;
}

function collectFiles() {
  const files = [];
  for (const d of SCAN_DIRS) {
    const full = path.join(ROOT, d);
    if (fs.existsSync(full)) walk(full, files);
  }
  return files;
}

const envKeys = new Set();
const findings = []; // { rel, line, kind, snippet, suggested }

function suggestName(kind, snippet) {
  if (/supabase/i.test(snippet)) return kind === 'url' ? 'NEXT_PUBLIC_SUPABASE_URL' : 'SUPABASE_KEY';
  if (kind === 'url') {
    const host = (snippet.match(/^https?:\/\/([^/]+)/) || [])[1] || '';
    const label = host.split('.').filter((p) => p !== 'www' && p.length > 2)[0] || 'external';
    return `NEXT_PUBLIC_${label.replace(/[^A-Za-z0-9]/g, '_').toUpperCase()}_URL`;
  }
  return 'SECRET_VALUE';
}

function scan(file) {
  let text;
  try {
    text = fs.readFileSync(file, 'utf8');
  } catch {
    return;
  }
  const rel = path.relative(ROOT, file).replace(/\\/g, '/');

  for (const m of text.matchAll(/process\.env\.([A-Z0-9_]+)/g)) envKeys.add(m[1]);
  for (const m of text.matchAll(/process\.env\[\s*['"`]([A-Za-z0-9_]+)['"`]\s*\]/g)) envKeys.add(m[1]);

  text.split(/\r?\n/).forEach((line, i) => {
    if (/^\s*(\/\/|\*|\/\*)/.test(line)) return; // comments
    const lineNo = i + 1;

    // A URL on a line that reads process.env is a documented fallback default,
    // not hardcoded config — same exclusion the secret-assignment check uses.
    if (!/process\.env/.test(line)) {
      for (const m of line.matchAll(/['"`](https?:\/\/[^'"`\s]+)['"`]/g)) {
        const url = m[1];
        if (URL_ALLOWLIST.test(url)) continue;
        findings.push({ rel, line: lineNo, kind: 'url', snippet: url, suggested: suggestName('url', url) });
      }
    }

    // Long opaque strings: JWTs, publishable keys, hex/base64 secrets.
    for (const m of line.matchAll(/['"`](eyJ[A-Za-z0-9_-]{20,}|sb_[a-z]+_[A-Za-z0-9_-]{16,}|sk-[A-Za-z0-9_-]{16,}|[A-Fa-f0-9]{40,})['"`]/g)) {
      findings.push({ rel, line: lineNo, kind: 'secret', snippet: m[1].slice(0, 12) + '…', suggested: suggestName('secret', line) });
    }

    // key/secret/token/password assigned a non-empty string literal.
    const assign = line.match(
      /\b([A-Za-z_$][A-Za-z0-9_$]*)\s*[:=]\s*['"`]([^'"`\n]{8,})['"`]/
    );
    if (assign && SECRETISH_KEY.test(assign[1]) && !/process\.env/.test(line)) {
      findings.push({
        rel,
        line: lineNo,
        kind: 'secret',
        snippet: `${assign[1]} = "${assign[2].slice(0, 8)}…"`,
        suggested: assign[1].replace(/([a-z0-9])([A-Z])/g, '$1_$2').replace(/[^A-Za-z0-9]/g, '_').toUpperCase(),
      });
    }
  });
}

/** Keep values the user already wrote into .env.example. */
function existingValues() {
  const map = new Map();
  if (!fs.existsSync(OUT)) return map;
  for (const line of fs.readFileSync(OUT, 'utf8').split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Za-z0-9_]+)\s*=(.*)$/);
    if (m) map.set(m[1], m[2]);
  }
  return map;
}

function build(prev) {
  const lines = [
    '# Auto-generated by .claude/hooks/env-example-sync.js',
    '# Variable contract for this project. Copy to .env.local and fill in real values.',
    '# Never commit real secrets — .env* is gitignored except this file.',
    '',
  ];

  for (const key of [...envKeys].sort()) {
    lines.push(`${key}=${prev.get(key) ?? ''}`);
  }

  if (findings.length) {
    const seen = new Set();
    lines.push('', '# --- TODO: hardcoded config detected in source — move these into env ---');
    for (const f of findings) {
      const id = `${f.rel}:${f.line}:${f.suggested}`;
      if (seen.has(id)) continue;
      seen.add(id);
      lines.push(`# ${f.rel}:${f.line}  ${f.snippet}`);
      if (!envKeys.has(f.suggested)) lines.push(`# ${f.suggested}=`);
    }
  }

  return lines.join('\n') + '\n';
}

try {
  collectFiles().forEach(scan);

  if (envKeys.size === 0 && findings.length === 0) process.exit(0);

  const next = build(existingValues());
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : null;

  if (next === current) process.exit(0);

  fs.writeFileSync(OUT, next, 'utf8');

  const parts = [`.env.example updated — ${envKeys.size} env var(s)`];
  if (findings.length) {
    const uniq = [...new Set(findings.map((f) => `${f.rel}:${f.line} (${f.snippet})`))];
    parts.push(`${uniq.length} hardcoded config value(s) found: ${uniq.slice(0, 5).join(', ')}`);
  }
  process.stdout.write(JSON.stringify({ systemMessage: `[env-example] ${parts.join(' | ')}` }));
} catch {
  process.exit(0); // never block the session
}
